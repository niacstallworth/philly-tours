import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const citiesRoot = path.join(repoRoot, "cities");
const outputPath = path.join(repoRoot, "src", "city-runtime", "registry.ts");

function toCamelCase(value) {
  return value
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((segment, index) => {
      const lower = segment.toLowerCase();
      if (index === 0) {
        return lower;
      }
      return lower.charAt(0).toUpperCase() + lower.slice(1);
    })
    .join("");
}

const cityIds = fs
  .readdirSync(citiesRoot, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort();

if (cityIds.length === 0) {
  throw new Error("No city packs found under cities/.");
}

const importLines = [];
const registryEntries = [];

for (const cityId of cityIds) {
  const prefix = toCamelCase(cityId);
  importLines.push(`import ${prefix}CityJson from "../../cities/${cityId}/city.json";`);
  importLines.push(`import ${prefix}BrandingJson from "../../cities/${cityId}/branding.json";`);
  importLines.push(`import ${prefix}SeoJson from "../../cities/${cityId}/seo.json";`);
  importLines.push(`import ${prefix}SocialJson from "../../cities/${cityId}/social.json";`);
  importLines.push(`import ${prefix}BusinessProfileJson from "../../cities/${cityId}/business-profile.json";`);
  importLines.push(`import ${prefix}ToursJson from "../../cities/${cityId}/tours.json";`);
  importLines.push(`import ${prefix}NarrationJson from "../../cities/${cityId}/narration.json";`);
  importLines.push(`import ${prefix}ArJson from "../../cities/${cityId}/ar.json";`);

  registryEntries.push(
    [
      `  ${cityId}: {`,
      `    city: ${prefix}CityJson,`,
      `    branding: ${prefix}BrandingJson,`,
      `    seo: ${prefix}SeoJson,`,
      `    social: ${prefix}SocialJson,`,
      `    businessProfile: ${prefix}BusinessProfileJson,`,
      `    tours: ${prefix}ToursJson as Tour[],`,
      `    narration: ${prefix}NarrationJson as CityNarrationPack,`,
      `    ar: ${prefix}ArJson as Record<string, CityArEntry>`,
      `  }`
    ].join("\n")
  );
}

const supportedCityUnion = cityIds.map((cityId) => `"${cityId}"`).join(" | ");
const supportedCityList = cityIds.map((cityId) => `"${cityId}"`).join(", ");
const defaultCityId = cityIds.includes("philly") ? "philly" : cityIds[0];

const content = [
  "// This file is auto-generated by scripts/generate-city-runtime-registry.mjs.",
  "// Regenerate it whenever city packs are added or removed.",
  "",
  ...importLines,
  'import type { Tour } from "../types";',
  "",
  `export type SupportedCityId = ${supportedCityUnion};`,
  "",
  'export const SUPPORTED_CITY_IDS = [' + supportedCityList + "] as const;",
  `export const DEFAULT_CITY_ID: SupportedCityId = "${defaultCityId}";`,
  "",
  'export type CityNarrationVariant = "drive" | "walk";',
  "",
  "export type CityNarrationEntry = {",
  "  drive?: string;",
  "  walk?: string;",
  "};",
  "",
  "export type CityNarrationPack = {",
  "  catalogByStopId: Record<string, CityNarrationEntry>;",
  "  scriptMapByStopId: Record<string, CityNarrationEntry>;",
  "};",
  "",
  "export type CityArEntry = {",
  "  id: string;",
  "  title: string;",
  "  arType: string;",
  "  assetNeeded?: string;",
  "  modelUrl?: string;",
  "  estimatedEffort?: string;",
  "  webModelAvailable?: boolean;",
  "  iosModelAvailable?: boolean;",
  "};",
  "",
  "export type CityConfig = {",
  "  id: string;",
  "  name: string;",
  "  shortName: string;",
  "  cityName: string;",
  "  stateCode: string;",
  "  countryCode: string;",
  "  timezone: string;",
  "  slug: string;",
  "  websiteOrigin: string;",
  "  serviceArea: string[];",
  "  defaultMapCenter: { lat: number; lng: number; zoom: number };",
  "  defaultTourId: string;",
  "  launchStatus?: string;",
  "  [key: string]: unknown;",
  "};",
  "",
  "export type CityBranding = {",
  "  primary: string;",
  "  secondary: string;",
  "  heroEyebrow: string;",
  "  heroTitle: string;",
  "  heroBody: string;",
  "  [key: string]: unknown;",
  "};",
  "",
  "export type CitySeo = {",
  "  homeTitle: string;",
  "  homeDescription: string;",
  "  catalogTitle: string;",
  "  catalogDescription: string;",
  "  homePanelEyebrow: string;",
  "  homePanelTitle: string;",
  "  homePanelBody: string;",
  "  searchImage: string;",
  "  organizationType: string[];",
  "  knowsAbout: string[];",
  "  homePanelHighlights: string[];",
  "  [key: string]: unknown;",
  "};",
  "",
  "export type CitySocial = {",
  "  sameAs: string[];",
  "  [key: string]: unknown;",
  "};",
  "",
  "export type CityBusinessProfile = {",
  "  businessName: string;",
  "  website: string;",
  "  primaryCategory: string;",
  "  shortDescription: string;",
  "  fullDescription: string;",
  "  [key: string]: unknown;",
  "};",
  "",
  "export type CityPackRuntime = {",
  "  city: CityConfig;",
  "  branding: CityBranding;",
  "  seo: CitySeo;",
  "  social: CitySocial;",
  "  businessProfile: CityBusinessProfile;",
  "  tours: Tour[];",
  "  narration: CityNarrationPack;",
  "  ar: Record<string, CityArEntry>;",
  "};",
  "",
  "const CITY_REGISTRY: Record<SupportedCityId, CityPackRuntime> = {",
  registryEntries.join(",\n"),
  "};",
  "",
  "export function isSupportedCityId(value: string): value is SupportedCityId {",
  '  return (SUPPORTED_CITY_IDS as readonly string[]).includes(value);',
  "}",
  "",
  "export function getCityPackById(cityId: string): CityPackRuntime {",
  "  return CITY_REGISTRY[isSupportedCityId(cityId) ? cityId : DEFAULT_CITY_ID];",
  "}",
  ""
].join("\n");

fs.writeFileSync(outputPath, content);
console.log(`Generated ${path.relative(repoRoot, outputPath)} for ${cityIds.length} cities.`);
